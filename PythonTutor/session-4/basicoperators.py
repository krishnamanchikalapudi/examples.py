num1 = 21
num2 = 2
num3 = 23

# add
print('num1 + num2 = ', 100 + num2)
# sub
print('num1 - num2 = ', num1 - num2)
# multiply
print('num1 * num2 = ', num1 * num2)
# division
print('num1 / num2 = ', num1 / num2)
# reminder = 1
print('num3 % num2 = ', num3 % num2)


# Power 2 is Squared
print('num2 ** 2 = ', num2 ** 2)
# Power 3 is cubed
print('num2 ** 3 = ', num2 ** 3)

print('num2 ** 4 = ', num2 ** 4)


# Operators with String
helloworld = "hello "  + "world"

hello = "hello"
world = "world"

print("helloworld = ", helloworld)
print("helloworld 1 = ", hello, world)
lotsofhellos = "hello " * 10
