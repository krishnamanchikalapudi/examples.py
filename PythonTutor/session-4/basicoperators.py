num1 = 10
num2 = 5

# add
print('num1 + num2 = ', num1 + num2)
# sub
print('num1 - num2 = ', num1 - num2)
# multiply
print('num1 * num2 = ', num1 * num2)
# division
print('num1 / num2 = ', num1 / num2)
# reminder
print('num1 % num2 = ', num1 % num2)

# Power 2 is Squared
print('num2 ** 2 = ', num2 ** 2)
# Power 3 is cubed
print('num2 ** 3 = ', num2 ** 3)

# Operators with String
helloworld = "hello" + " " + "world"
print("helloworld = ", helloworld)
lotsofhellos = "hello " * 10
print("lotsofhellos = ", lotsofhellos)